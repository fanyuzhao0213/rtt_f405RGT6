//清屏
void OLED_Clear(struct rt_i2c_bus_device *bus)
{

    rt_uint8_t i,n;
    for(i=0;i<8;i++)
    {
        write_reg(bus,Command_ADDR,0xb0+i);
        write_reg(bus,Command_ADDR,0x00);
        write_reg(bus,Command_ADDR,0x10);
        for(n=0;n<128;n++) write_reg(bus,Data_ADDR,0);
    }
}

//坐标设置
void OLED_Set_Pos(struct rt_i2c_bus_device *bus,rt_uint8_t x,rt_uint8_t y)
{
    write_reg(bus,Command_ADDR,0xb0+y);
    write_reg(bus,Command_ADDR,((x&0xf0)>>4)|0x10);
    write_reg(bus,Command_ADDR,(x&0x0f));
}

